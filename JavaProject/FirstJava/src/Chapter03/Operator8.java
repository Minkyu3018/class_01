package Chapter03;

public class Operator8 {

	public static void main(String[] args) {
		
		// 산술연산자 - 문자형 증감 연산 패턴
	
		char c = 'A';
				System.out.println(c++); //출력 : A, 저장 : B
				System.out.println(c--); //출력 : B, 저장 : A
								
				System.out.println(c); //출력 : A
				
				System.out.println(++c); //출력 : B , 저장 : A
				System.out.println(--c); //출력 : A
		
	}
}
