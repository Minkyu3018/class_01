package alone;

public class Variable01 {

	public static void main(String[] args) {
		
		// 변수의 선언
		int age;
		age = 23;
		System.out.println(age);
		
		// 변수의 초기화(변경)
		age = age + 2;
		System.out.println(age);
		System.out.println();
		
		// 변수 변경 , a와 b의 값으로 저장하고 숫자만 변경
		int a = 18 , b = 3;
		System.out.println( a + b );
		System.out.println( a - b );
		System.out.println( a * b );
		System.out.println( a / b );
		
	}

}
